<h1>The Web is a Big Place</h1>
<p>
Send content to any service on the web.  Locate new services automatically.  Personalize the services available to users.  
</p>
<p>
Sharing to Facebook?  Sure.  But what about posting to your favorite off-beat forum?  Or translate and print?  What if your browser and your sharing tools could automatically figure out what services make sense for you? 
</p>
<p>
</p>
<p>
OExchange is an open specification to facilitate content sharing on the web as a whole.  It defines:
<ul>
	<li>A simple method for sending URL-based content to a website (referred to as a Target)</li>
	<li>A mechanism for specifying and discovering these Targets automatically</li>
	<li>A method for users to register personal Target preferences</li>	
</ul>
</p>
<p>
	Take a look at a few <a href="demo">demos</a>, or jump right into <a href="/spec">the specification</a>.
</p>	   

